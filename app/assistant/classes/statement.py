class Statement:
    def __init__(self):
        self.description = None
        self.order = None
        self.thought = None
        self.about = None

    def setDescription(self, description):
        self.description = description

    def setOrder(self, order):
        self.order = order

    def setThought(self, thought):
        self.thought = thought

    def setAbout(self, about):
        self.about = about
